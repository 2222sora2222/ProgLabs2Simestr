#include <cmath>
/*Объяснение работы программы:
Ввод данных: Пользователь вводит массу тела m, приложенную силу F0, угол α (в градусах) и коэффициент трения µ.

Перевод угла в радианы: Угол переводится в радианы для использования в математических функциях sin и cos.

Расчет нормальной силы N: Учитывается вертикальная составляющая силы F0 * sin(α). Если нормальная сила становится отрицательной, 
она устанавливается в 0.

Горизонтальная составляющая силы: Рассчитывается как F0 * cos(α).

Максимальная сила трения покоя: Вычисляется по формуле µ * N.

Определение ускорения: С помощью тернарного оператора проверяется, превышает ли горизонтальная составляющая силу трения.
 Если да, ускорение рассчитывается по формуле (F_horizontal - F_friction_max) / m, иначе ускорение равно 0.
 
 m = 10 кг, F0 = 50 Н, α = 30 градусов, µ = 0.2.
 m = 10 кг, F0 = 50 Н, α = 30 градусов, µ = 0.2.

alpha_rad = 30 * pi / 180 ≈ 0.5236 рад.

sin(30) = 0.5, cos(30) ≈ 0.8660.

N = 10*9.81 - 50*0.5 = 98.1 - 25 = 73.1 Н.

F_friction_max = 0.2 * 73.1 ≈ 14.62 Н.

F_horizontal = 50 * 0.866 ≈ 43.3 Н.

43.3 > 14.62 → a = (43.3 - 14.62)/10 ≈ 28.68 / 10 ≈ 2.868 м/с².

Пример 2:

m = 10 кг, F0 = 100 Н, α = 90 градусов (сила направлена вверх).

alpha_rad = pi/2 ≈ 1.5708 рад.

sin(90) = 1, cos(90)=0.

N = 10*9.81 - 100*1 = 98.1 - 100 = -1.9 → N=0.

F_friction_max = 0.2 * 0 = 0.

F_horizontal = 100 * 0 = 0.

0 > 0 → нет. a=0.

Но если F0 = 100 Н, α = 0 градусов (горизонтальное направление):

N = 10*9.81 - 100*0 = 98.1.

F_friction_max = 0.2 * 98.1 ≈ 19.62.

F_horizontal = 100*1 = 100.

100 > 19.62 → a = (100 -19.62)/10 ≈ 8.038 м/с².*/
using namespace std;

int main() {
    double m, F0, alpha_deg, mu;
    const double g = 9.81;

    // Ввод данных
    cout << "Введите массу тела (кг): ";
    cin >> m;
    cout << "Введите приложенную силу F0 (Н): ";
    cin >> F0;
    cout << "Введите угол α (градусы): ";
    cin >> alpha_deg;
    cout << "Введите коэффициент трения µ: ";
    cin >> mu;

    // Переводим угол в радианы
    double alpha_rad = alpha_deg * M_PI / 180.0;

    // Рассчитываем нормальную реакцию опоры
    double N = m * g - F0 * sin(alpha_rad);
    N = (N < 0) ? 0 : N; // Нормальная сила не может быть отрицательной

    // Горизонтальная составляющая силы
    double F_horizontal = F0 * cos(alpha_rad);

    // Максимальная сила трения покоя
    double F_friction_max = mu * N;

    // Расчет ускорения с использованием тернарного оператора
    double a = (F_horizontal > F_friction_max) ? (F_horizontal - F_friction_max) / m : 0.0;

    // Вывод результата
    cout << "Ускорение тела: " << a << " м/с²" << endl;

    return 0;
}